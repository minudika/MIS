public class Class {
    private int grade;
    private String className;
    private String classTeacherNic;
    private String ID;
    private int numberOfStudents;

    /**
     * @return the grade
     */
    public int getGrade() {
        return grade;
    }

    /**
     * @param grade the grade to set
     */
    public void setGrade(int grade) {
        this.grade = grade;
    }

    /**
     * @return the className
     */
    public String getClassName() {
        return className;
    }

    /**
     * @param className the className to set
     */
    public void setClassName(String className) {
        this.className = className;
    }

    /**
     * @return the classTeacherNic
     */
    public String getClassTeacherNic() {
        return classTeacherNic;
    }

    /**
     * @param classTeacherNic the classTeacherNic to set
     */
    public void setClassTeacherNic(String classTeacherNic) {
        this.classTeacherNic = classTeacherNic;
    }

    /**
     * @return the ID
     */
    public String getID() {
        return ID;
    }

    /**
     * @param ID the ID to set
     */
    public void setID(String ID) {
        this.ID = ID;
    }

    /**
     * @return the numberOfStudents
     */
    public int getNumberOfStudents() {
        return numberOfStudents;
    }

    /**
     * @param numberOfStudents the numberOfStudents to set
     */
    public void setNumberOfStudents(int numberOfStudents) {
        this.numberOfStudents = numberOfStudents;
    }
    
            
}
